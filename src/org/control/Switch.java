package org.control;

public class Switch {
public static void main(String[] args) {
	
	int student = 8;
	
	switch (student) {
	case 1:
		System.out.println("Ram");
		break;
	

	case 2:
		System.out.println("Raju");
		break;

	case 3:
	    System.out.println("Dinesh");
	break;

	case 4:
		System.out.println("Suresh");
	
	break;

	default:
		System.out.println("Person Invalid-Type below 4");
		break;
	}
}
}
